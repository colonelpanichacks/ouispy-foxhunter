// RSSI-based timing (milliseconds between beeps) - START AT 3 SECONDS
// Ultra strong (-20 to -40): 3000ms to 1000ms (3s to 1s)
// Strong signal (-40 to -55): 1000ms to 500ms (1s to 0.5s)
// Medium signal (-55 to -70): 500ms to 200ms (0.5s to 0.2s)
// Weak signal (-70 to -85): 200ms to 100ms (0.2s to 0.1s)
// Very weak (-85 to -95): 100ms to 50ms (0.1s to 0.05s)
// Ultra weak (-95+): 50ms (LIGHTNING FAST)
int calculateBeepInterval(int rssi) {
    // Map RSSI to beep intervals: 5 seconds (weak) to 100ms (strong)
    // RSSI ranges: -95 (very weak) to -30 (very strong)
    if (rssi >= -40) {
        return map(rssi, -40, -30, 1000, 100); // 1s to 100ms for very strong signals
    } else if (rssi >= -50) {
        return map(rssi, -50, -40, 2000, 1000); // 2s to 1s
    } else if (rssi >= -60) {
        return map(rssi, -60, -50, 3000, 2000); // 3s to 2s
    } else if (rssi >= -70) {
        return map(rssi, -70, -60, 4000, 3000); // 4s to 3s
    } else if (rssi >= -80) {
        return map(rssi, -80, -70, 4500, 4000); // 4.5s to 4s
    } else {
        return 5000; // 5 seconds for very weak signals (-80 and below)
    }
}

// Buzzer functions
void singleBeep() {
    ledcWrite(0, BUZZER_DUTY);
    delay(100);
    ledcWrite(0, 0);
}

void ascendingBeeps() {
    // Ready signal - 2 fast ascending beeps with close melodic notes
    ledcWriteTone(0, 1900);
    ledcWrite(0, BUZZER_DUTY);
    delay(150);
    ledcWrite(0, 0);
    delay(50);
    
    ledcWriteTone(0, 2200);
    ledcWrite(0, BUZZER_DUTY);
    delay(150);
    ledcWrite(0, 0);
    
    // Reset to normal frequency and ENSURE buzzer is OFF
    ledcWriteTone(0, BUZZER_FREQ);
    ledcWrite(0, 0);  // Make sure buzzer is completely off
    
    // Add delay to prevent interference with proximity beeps
    delay(500);
}

void threeSameToneBeeps() {
    // Three beeps at same tone for initial detection
    for (int i = 0; i < 3; i++) {
        ledcWriteTone(0, 2000); // Same tone for all three
        ledcWrite(0, BUZZER_DUTY);
        delay(100);
        ledcWrite(0, 0);
        delay(50);
    }
    
    // Reset to normal frequency and ENSURE buzzer is OFF
    ledcWriteTone(0, BUZZER_FREQ);
    ledcWrite(0, 0);
    
    // Add delay to prevent interference with proximity beeps
    delay(500);
}

void proximityBeep() {
    // Ultra-fast beep for realtime response
    ledcWrite(0, BUZZER_DUTY);
    delay(30); // Shorter beep for faster response
    ledcWrite(0, 0);
}

// Configuration storage
void saveConfiguration() {
    preferences.begin("tracker", false);
    preferences.putString("targetMAC", targetMAC);
    preferences.end();
    Serial.println("Configuration saved to NVS");
}

void loadConfiguration() {
    preferences.begin("tracker", true);
    targetMAC = preferences.getString("targetMAC", "");
    preferences.end();
    
    if (targetMAC.length() > 0) {
        Serial.println("Configuration loaded from NVS");
        Serial.println("Target MAC: " + targetMAC);
    }
}

